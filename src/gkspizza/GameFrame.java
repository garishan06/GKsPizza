        panStartScreen.setVisible(false);
        panGame = new GamePanel(this);
        this.add(getPanGame());

        panGame.setSize(getPanGame().getPreferredSize());
        panGame.setLocation(0, 0);
        panGame.setVisible(true);

        //starts game loop
        panGame.startGameThread();
        
