    /**
     *  This method is used to draw anything on to this game frame. For our purposes, it is used to draw our custom title screen image
     * @param g a basic painting tool. It is used to create a more sophisticated and efficient painting tool from the Graphics2D class, "g2". 
     */
    public void draw(Graphics g) {
        Graphics2D g2 = (Graphics2D) g;

        g2.drawImage(this.getTitleScreen(), 0, 0, this.getWidth(), this.getHeight(), null);

    }

    /**
     * this method is used to load in any images that need to be drawn in our frame. We primarily use it to load our game's title screen
     */
    private void loadImages() {
        try {
            this.titleScreen = ImageIO.read(getClass().getResourceAsStream("/images/screens/title screen.png"));
        } catch (IOException ex) {
            Logger.getLogger(GameFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

